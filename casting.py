x = str(4) #4 is a string in x as "4"
z = float("5") #5 is a float in z as 5.0
y = int(8) #8 is an integer in y as 8

"""
The below will be used ti get the types of variables
"""

print(type(x))
print(type(z))
print(type(y))

"""
single and double quote are both strings
"""

